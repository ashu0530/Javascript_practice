let n = 10;  
while (n>0){         // loop run untill n>0 
    console.log("*"); //print *
    n-= 1;  //   n=n-1 here condition is n=n-1 so it will print vertically * 10 times
     
}

let m = 10;  
while (m>0){         // loop run untill m>0 
    console.log("*"); //print *
    m= m-2;  //   m=m-2 here condition  so it will print vertically * 5 times
     
}

